# Task 1.3
i = 1  # Start from 1
while i <= 3:  # Run loop until i reaches rows
    print("🍦" * i)  # Print pattern repeated i times
    i += 1  # Increment i for the next line

# Task 1.4
for i in range(1, 3 + 1):  # Loop from 1 to the number of rows (inclusive)
    print("🍦" * i)
